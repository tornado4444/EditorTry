void Texture::bind() {
	glActiveTexture(GL_TEXTURE0 + unit);
	glBindTexture(type, ID);
}
void Texture::unbind() {
	glBindTexture(type, 0);
}

void Texture::deleteShader() {
	glDeleteTextures(1, &ID);
}